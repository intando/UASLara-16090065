            <p>Berikut adalah beberapa tempat wisata yang paling populer selama 2018</p>
            <p>Kota Seoul menawarkan keunikan budaya yang sangat menarik dimana terdapat banyak tradisi, inovasi serta kehidupan tradisional dan modern yang saling berdampingan. Tak hanya itu alam serta iklimnya pun membuat wisatawan ingin berwisata ke Seoul. Seoul merupakan pusat tujuan wisatawan yang datang ke Korea Selatan.</p>
            <p> 1. Istana Gyeongbokgung </p>
                <p> Istana Gyeongbokgung adalah tempat tinggal resmi utama keluarga kerajaan selama Dinasti Joseon. Membawa makna "tempat di mana raja baru dapat menikmati kemakmuran dan berkat yang banyak", istana ini adalah istana pertama yang dibangun selama Dinasti Joseon. Istana ini unik karena wilayahnya yang luas dan bangunannya yang harmonis, dan merupakan satu-satunya istana yang keempat gerbang utamanya masih ada hingga saat ini. Sebelum memasuki istana, pengunjung dapat berkumpul di Gerbang Gwanghwamun untuk menyaksikan Upacara Pergantian Pengawal Kerajaan. Tips-nya, atur waktu kunjungan agar dapat melihat pertunjukan budaya ini pada saat berkunjung. Istana Gyeongbokgung juga sesekali menawarkan kunjungan malam khusus, jadi jangan lewatkan kesempatan untuk menikmati suasana malam di halaman istana</p>
            <br><br>
            <p> 2. Menara Namsan Seoul </p>
                <p> Landmark paling popler ini terletak di puncak Gunung Namsan, yang berada di pusat kota Seoul. Menara ini menarik pengunjung sepanjang tahun karena terkenal dengan pemandangan kota Seoul yang memesona. Menara ini tidak hanya memiliki dek observasi, tetapi juga berbagai kafe dan restoran yang cocok untuk berkumpul bersama.</p>
            <br><br>
            <p> 3. Lotte World </p>
                <p> Taman hiburan ini buka sepanjang tahun dan memiliki berbagai macam acara dan pertunjukan. Lotte World terletak di pusat kota Seoul dan mudah diakses dengan transportasi umum. Lotte World dibagi menjadi dua area: Adventure (Petualangan) terletak di dalam ruangan, dan Magic Island (Pulau Ajaib) di luar. Arena Petualangan mencakup sejumlah wahana, termasuk komidi putar, kapal bajak laut Spanyol, flume ride dan banyak lagi, sementara Magic Island memiliki wahana raksasa seperti Gyro Drop dan Gyro Spin. Lotte World Tower Seoul Sky dan Lotte World Shopping Mall berada di dekat Lotte World.</p>
            <br><br>
            <p> 4. Desa Hanok Bukchon </p>
                <p> Desa Hanok Bukchon menunjukkan pesona bangunan tradisional Korea. Sebagai lingkungan tempat kerabat keluarga kerajaan tinggal pada masanya, maka Hanok di sini sangat megah. Untuk memaksimalkan kunjungan kesini, pengunjung disarankan untuk menyewa pakaian tradisional Hanbok supaya dapat merasakan suasana tradisional Korea dan dokumentasi yang unik.</p>
            <br><br>
            <p> 5. Pulau Nami </p>
                <p> Pulau Nami menjadi objek wisata yang populer setelah muncul dalam drama Korea “Winter Sonata”. Awalnya hanya sebuah puncak bukit yang disulap menjadi sebuah pulau dengan dibangunnya Bendungan Cheongpyeong, yang meningkatkan ketinggian air di sekitarnya. Pulau ini cukup kecil, sehingga pengunjung dapat mengelilingi pulau ini dalam waktu setengah hari.</p>
